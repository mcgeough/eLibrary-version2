package business;

/**
 *
 * @author mcgeo
 */
public class Review {

    private int id;
    private int rating;
    private String review;

    public Review(int id, int rating, String review) {
        this.id = id;
        this.rating = rating;
        this.review = review;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }

    public String getReview() {
        return review;
    }

    public void setReview(String review) {
        this.review = review;
    }

}
